    public void clear_text(boolean usuario, boolean contraseña, boolean focus_usuario) {
        if (usuario) {
            txtUsuario.setText("");
        }
        if (contraseña) {
            txtContraseña.setText("");
        }
        if (focus_usuario) {
            txtUsuario.requestFocus();
        } else {
            txtContraseña.requestFocus();
        }
    }

