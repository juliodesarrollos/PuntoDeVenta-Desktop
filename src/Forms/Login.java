    public class login implements Runnable {

        JFrame panel;

        public void show(JFrame pnl) {
            panel = pnl;
            new Thread(this).start();
        }

        @Override
        public void run() {
            mostrar_loading(true);
            try {
                if (CRUD.conexion_internet()) {
                    if (!txtUsuario.getText().equals("")) {
                        if (txtContraseña.getPassword().length > 0) {
                            try {
                                String pass = String.valueOf(txtContraseña.getPassword());
                                System.out.println(pass);
                                Object object_result = CRUD.login(txtUsuario.getText(), pass);
                                if (object_result instanceof Vendedor) {
                                    mostrar_loading(false);
                                    Vista.mensaje_informacion(Strings.bienvenido + Vendedor.recuperar_instancia().getNombre_vendedor(), Strings.login_correcto);
                                    Principal form_padre = new Principal();
                                    form_padre.setVisible(true);
                                    panel.setVisible(false);
                                } else {
                                    String result = (String) object_result;
                                    if (result.equals(Strings.error_password)) {
                                        mostrar_loading(false);
                                        Vista.mensaje_error(Strings.mensaje_error_password, Strings.login_incorrecto);
                                        clear_text(false, true, false);
                                    } else if (result.equals(Strings.error_usuario)) {
                                        mostrar_loading(false);
                                        Vista.mensaje_error(Strings.mensaje_error_usuario, Strings.login_incorrecto);
                                        clear_text(true, true, true);
                                    } else {
                                        mostrar_loading(false);
                                        Vista.mensaje_error(Strings.mensaje_error_desconocido, Strings.login_incorrecto);
                                        clear_text(true, true, true);
                                    }
                                }
                            } catch (SQLException | ClassNotFoundException ex) {
                                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                                mostrar_loading(false);
                                Vista.mensaje_error(Strings.mensaje_error_desconocido, Strings.login_incorrecto);
                                clear_text(true, true, true);
                            }
                        } else {
                            mostrar_loading(false);
                            Vista.mensaje_error(Strings.ingrese_contraseña, Strings.campos_vacios);
                            clear_text(false, false, false);
                        }
                    } else {
                        mostrar_loading(false);
                        Vista.mensaje_error(Strings.ingrese_usuario, Strings.campos_vacios);
                        clear_text(false, false, true);
                    }
                } else {
                    mostrar_loading(false);
                    Vista.mensaje_error(Strings.sin_internet, Strings.titulo_sin_internet);
                }
            } catch (InterruptedException | IOException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                mostrar_loading(false);
            }
        }

    }

